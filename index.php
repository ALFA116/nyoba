    <script>
        let lastScrollTop = 0;
        const navbar = document.querySelector('.navbar-fixed');
        const navbarHeight = navbar.offsetHeight;

        window.addEventListener('scroll', () => {
            let scrollTop = window.pageYOffset || document.documentElement.scrollTop;

            if (scrollTop > lastScrollTop) {
                // Scroll ke bawah
                navbar.style.transform = `translateY(-${Math.min(navbarHeight - 10, scrollTop)}px)`;
            } else {
                // Scroll ke atas
                navbar.style.transform = 'translateY(0)';
            }

            lastScrollTop = scrollTop;
        });

        window.addEventListener('scroll', function() {
            var navbar = document.getElementById('navbar');
            if (window.scrollY > 0) {
                navbar.classList.add('scrolled');
            } else {
                navbar.classList.remove('scrolled');
            }
        });

        // Initial state on page load
        window.addEventListener('load', function() {
            var navbar = document.getElementById('navbar');
            if (window.scrollY > 0) {
                navbar.classList.add('scrolled');
            }
        });

        const aboutUsButton = document.querySelector('.about-button');
        const popup = document.getElementById('aboutUsPopup');
        const closeButton = document.querySelector('.close');

        aboutUsButton.addEventListener('click', function(e) {
            e.preventDefault();
            popup.style.display = 'block';
        });

        closeButton.addEventListener('click', function() {
            popup.style.display = 'none';
        });

        window.addEventListener('click', function(e) {
            if (e.target == popup) {
                popup.style.display = 'none';
            }
        });

        // Slider for reviews

        let currentReview = 0;
        const reviews = document.querySelectorAll('.review');

        function changeReview(direction) {
            reviews[currentReview].classList.remove('active');
            currentReview = (currentReview + direction + reviews.length) % reviews.length;
            reviews[currentReview].classList.add('active');
        }

        // Tampilkan ulasan pertama saat halaman dimuat
        document.addEventListener('DOMContentLoaded', () => {
            reviews[currentReview].classList.add('active');

            // Ganti ulasan otomatis setiap 3 detik
            setInterval(() => {
                changeReview(1); // Ganti ke ulasan berikutnya
            }, 3000); // 3000 ms = 3 detik
        });

        let hasAnimated = false;

        function animateNumbers() {
            const statNumbers = document.querySelectorAll('.stat-number');
            statNumbers.forEach(stat => {
                const target = +stat.getAttribute('data-target');
                let count = 0;
                const increment = Math.ceil(target / 215); // Mengatur kecepatan animasi

                const updateCount = () => {
                    if (count < target) {
                        count += increment;
                        stat.textContent = count + '+';
                        setTimeout(updateCount, 25); // Mengatur interval waktu
                    } else {
                        stat.textContent = target + '+';
                    }
                };

                updateCount();
            });
        }

        window.addEventListener('scroll', () => {
            const statsSection = document.querySelector('.work-stats');
            const sectionPosition = statsSection.getBoundingClientRect().top;
            const screenPosition = window.innerHeight;

            if (!hasAnimated && sectionPosition < screenPosition) {
                animateNumbers();
                hasAnimated = true; // Pastikan animasi hanya terjadi sekali
            }
        });

        window.addEventListener('scroll', () => {
            const contactSection = document.querySelector('.contact-section');
            const sectionPosition = contactSection.getBoundingClientRect().top;
            const screenPosition = window.innerHeight;

            if (sectionPosition < screenPosition) {
                contactSection.classList.add('fade-in'); // Add class to trigger animation
            }
        });

        document.getElementById('contactUsButton').addEventListener('click', function() {
            const contactSection = document.getElementById('contact');
            contactSection.style.display = contactSection.style.display === 'none' ? 'flex' : 'none';
        });
    </script>

